package com.example.ourcuisine.Model;

public class CuisineModel
{
    private String id, name, origin, description;
    public String getId()
    {
        return id;
    }
    public String getName()
    {
        return name;
    }
    public String getOrigin()
    {
        return origin;
    }
    public String getDescription()
    {
        return description;
    }
}